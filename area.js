function calculaAreaRetangulo(largura, altura) {
    // Calcula a área do retângulo: largura * altura
    var area = largura * altura;
    return area; 
}

// Exemplo de uso da função
var larguraRetangulo = 5;
var alturaRetangulo = 10;
var areaCalculada = calculaAreaRetangulo(larguraRetangulo, alturaRetangulo);

console.log("A área do retângulo é: " + areaCalculada);
